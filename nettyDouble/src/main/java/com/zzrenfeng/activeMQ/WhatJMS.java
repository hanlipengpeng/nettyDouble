package com.zzrenfeng.activeMQ;
/**
 * JMS（Java Messaging Service）是Java平台上有关面向消息中间件(MOM)的技术规范，它便于消息系统中的Java应用程序进行消息交换,
 * 并且通过提供标准的产生、发送、接收消息的接口简化企业应用的开发，翻译为Java消息服务。
 * JMS客户：生产或消费基于消息的Java的应用程序或对象。
 * JMS生产者：创建并发送消息的JMS客户。
 * JMS消费者：接收消息的JMS客户。
 * JMS消息：包括可以在JMS客户之间传递的数据的对象
 * JMS队列：一个容纳那些被发送的等待阅读的消息的区域。与队列名字所暗示的意思不同，消息的接受顺序并不一定要与消息的发送顺序相同。一旦一个消息被阅读，该消息将被从队列中移走。
 * JMS主题：一种支持发送消息给多个订阅者的机制。
 * 
 * 支持两种模型：
 * 1：点对点或队列模型
 * 2：发布者/订阅者模型
 * 
 * kafka严格来说不属于JMS，应为kafka的消息不是有序的。
 * 
 * @author hanlipeng
 *
 */
